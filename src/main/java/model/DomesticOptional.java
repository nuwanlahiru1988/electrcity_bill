package model;

public class DomesticOptional {
	
	private int id;
	private double time_range_start;
	private double time_range_end;
	private double price;
	private double fixed;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getTime_range_start() {
		return time_range_start;
	}
	public void setTime_range_start(double time_range_start) {
		this.time_range_start = time_range_start;
	}
	public double getTime_range_end() {
		return time_range_end;
	}
	public void setTime_range_end(double time_range_end) {
		this.time_range_end = time_range_end;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getFixed() {
		return fixed;
	}
	public void setFixed(double fixed) {
		this.fixed = fixed;
	}
	
	

}
