package model;

public class DomesticReligious {
	
	private int id;
	private int range_start;
	private int range_end;
	private double price;
	private double fixed;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRange_start() {
		return range_start;
	}
	public void setRange_start(int range_start) {
		this.range_start = range_start;
	}
	public int getRange_end() {
		return range_end;
	}
	public void setRange_end(int range_end) {
		this.range_end = range_end;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public double getFixed() {
		return fixed;
	}
	public void setFixed(double fixed) {
		this.fixed = fixed;
	}
	
	

}
